#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	const char	*buf;
	char		*ret;

	buf = src;
	ret = dst;
	while (len && *buf)
	{
		*(dst++) = *(buf++);
		len--;
	}
	while (len)
	{
		*(dst++) = '\0';
		len--;
	}
	return (ret);
}
